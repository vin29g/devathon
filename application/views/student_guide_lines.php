	<div class="row">
		<h3 align="center">Student's Guidelines</h3>
		<ol>
			<li>Students must submit their application within the deadline announced by the TAPS. Late submission of applications will not be entertained.</li>
			<li>They should continually check the TAPS notice board and the web site for updates and new Job Announcements.</li>
			<li>One should apply to an organization only if he/she is eligible and interested in taking up a job at the organization. No withdrawal of applications will be entertained.</li>
			<li>If short-listed for interview, students must follow the dress code as specified by the TAPS. </li>
			<li>Students must carry their certificate files along with two passport size photographs, two sets of photo copies of their certificates and two to three copies of their resume for the interview.</li>
			<li>The students have to ensure good conduct during the entire placement process and also maintain proper discipline when in and around the premises of TAPS.</li>
			<li>The students have to get their mobile phones switched off during their participation in placement drives</li>
			<li>Students can clarify their doubts with either the student coordinators or the placement officer.</li>
			<li>Students are advised to go through the company websites to get more insight about the company and judge their suitability to the job. Any queries must be clarified with the company officials during the PPT only.</li>

		</ol>
	</div>
</div>