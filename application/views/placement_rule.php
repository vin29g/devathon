					  <h3><font color="#003399">A. ELIGIBILITY & REGISTRATION</b></font></h3> <br />
					  </p><p align="justify">

					        1.  Only those students who have registered with the <strong>TAPS </strong> are eligible to participate in the placement activities.</br></br>

					        2.  Students pursuing their studies under sponsored category are not eligible for campus placement.Also student who came to India on study VISA are not eligible for campus placements.</br></br>
                            
                            3.  Students must carry their <strong>identity cards </strong>with them during the PPT/written test/group discussion/interviews, and produce the same whenever demanded by the recruiting team, the placement officer or the student coordinators.<br /> <br />

					        4.  Students are not allowed to interact with the company executives directly. Any queries must be clarified during the PPT itself. Subsequently, they can bring their doubts (if any) to the notice of the placement officer who will then take necessary action.<br/><br/>

                            5.  Any student found to have applied or secured a job through off campus application without the prior consent of the Placement Officer would be debarred from taking part in any further placement process. Further, the company in which he/she has secured a job, would also be informed of the policy violation by the student.<br/><br/>

                            6.  If a student is not joining an organization for unavoidable circumstances, then the accepted job offers must be surrendered to the Placement Officer.<br/><br/>
                            
                            7.  A student violating any of the above mentioned rules and regulations or found indulging in any act of indiscipline/misbehavior thereby earning a bad name to the Institute will be debarred from availing any further placement facilities and is liable for strict disciplinary action, as per the Institute rules and regulations.<br/><br/>

							8.	All students who expect to graduate from the Institute by the end of the academic year 2015-16 and are seeking employment may register for campus placements with this office.  Placement Registration is valid for ONE ACADEMIC YEAR ONLY.<br /><br />
								
							9.	Campus placement is a facility provided for the students.  Registration is not compulsory.  Students <strong>not interested</strong> in placement are advised not to register for placement.<br /><br />
							
							10.	<strong>Backlogs:</strong> Students having backlog of 4 or more courses are not permitted to register for placement.  Such students are advised to clear the backlogs and then register after the July-November end semester examinations.  However, the extended students can register in case of non-completion of course/project requirements in their last two semesters.<br /><br />
							
							
					
